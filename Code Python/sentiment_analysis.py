def clean_text(text):

    if isinstance(text, str):
        # 1. Lowercasing: Mengubah teks menjadi huruf kecil
        text = text.lower()

        # 2. Menghapus angka
        text = re.sub(r'\d+', '', text)

        # 3. Menghapus tanda baca
        text = text.translate(str.maketrans('', '', string.punctuation))

        # 4. Menghapus whitespace berlebih
        text = re.sub(r'\s+', ' ', text).strip()

        # 5. Menghapus karakter khusus (misalnya: emoji, simbol)
        text = re.sub(r'[^\x00-\x7F]+', '', text)

        # 6. Menghapus URL
        re.sub(r'http\S+|www\S+|https\S+', '', text, flags=re.MULTILINE)

        # 7. Menghapus kata berulang
        # Mendefinisikan r and max_repeat
        max_repeat = 2 # m Jumlah pengulangan maksimum untuk sebuah kata
        r = r'\b(\w+)\s+\1\b' # Regex ini akan menemukan kata-kata yang berulang
        text = re.sub(r, r'\1', text)

    # Jika bukan string (misalnya, NaN), kembalikan string kosong atau tangani sesuai kebutuhan
    else:
        text = ''  # atau text = str(teks) jika Anda ingin mengonversi ke string

    return text  # Pastikan untuk mengembalikan teks yang dimodifikasi

