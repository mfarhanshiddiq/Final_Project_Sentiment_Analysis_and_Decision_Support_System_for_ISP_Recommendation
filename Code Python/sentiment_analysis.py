import pandas as pd
import re
from collections import defaultdict

# Load data
data = pd.read_csv('HASIL-PREP.csv', sep=';', on_bad_lines='error', engine='python', encoding='latin-1')

import pandas as pd
import re
import matplotlib.pyplot as plt

# Fungsi untuk menghitung frekuensi kriteria
def calculate_criteria_frequencies(data):
    criteria_counts = {
        'harga': data['Cleaned_Text'].str.count('harga|pulsa|bonus|mahal|murah|biaya|tarif|diskon|hemat|nominal|bayar', flags=re.IGNORECASE).sum(),
        'jangkauan jaringan': data['Cleaned_Text'].str.count('jaringan|sinyal|4g|5g|lemot|stabil|putus|offline|online|kuota|operator|koneksi|akses|internet', flags=re.IGNORECASE).sum(),
        'kecepatan': data['Cleaned_Text'].str.count('cepat|lambat|respon|tunggu|proses|reaksi|responsif|delay|loading|buffering|latensi', flags=re.IGNORECASE).sum(),
        'layanan pelanggan': data['Cleaned_Text'].str.count('layan[an]?|cs|mimin|bantu|bantuan|tolong|dibantu|customer|pelayanan|ramah|solusi|keluhan|komplain|helpdesk|respon', flags=re.IGNORECASE).sum(),
        'fitur tambahan': data['Cleaned_Text'].str.count('fitur|aplikasi|myim|menu|paket|roaming|esim|wa[ ]?im|update|versi|notif|sms|telepon|internet|kuota|promo|bonus', flags=re.IGNORECASE).sum()
    }
    return criteria_counts

# Buat cleaned_text jika belum ada
if 'Cleaned_Text' not in data.columns:
    data['Cleaned_Text'] = data['Stemming'].apply(lambda x: ' '.join(eval(x)) if pd.notnull(x) else '')

# Hitung frekuensi kriteria
criteria_counts = calculate_criteria_frequencies(data)
total = sum(criteria_counts.values())
dynamic_weights = {k: v/total for k, v in criteria_counts.items()}

# Konversi ke DataFrame untuk visualisasi
weights_df = pd.DataFrame({
    'Kriteria': list(dynamic_weights.keys()),
    'Bobot': list(dynamic_weights.values()),
    'Frekuensi': list(criteria_counts.values())
}).sort_values('Bobot', ascending=False)

# Buat visualisasi
plt.figure(figsize=(12, 6))

# Plot frekuensi
plt.subplot(1, 2, 1)
bars1 = plt.bar(weights_df['Kriteria'], weights_df['Frekuensi'], color='skyblue')
plt.title('Frekuensi Kemunculan Kriteria', pad=20)
plt.xlabel('Kriteria')
plt.ylabel('Jumlah Kemunculan')
plt.xticks(rotation=45, ha='right')
plt.grid(axis='y', linestyle='--', alpha=0.7)

# Tambah label nilai di atas bar
for bar in bars1:
    height = bar.get_height()
    plt.text(bar.get_x() + bar.get_width()/2., height,
             f'{int(height)}',
             ha='center', va='bottom')

# Plot bobot
plt.subplot(1, 2, 2)
bars2 = plt.bar(weights_df['Kriteria'], weights_df['Bobot'], color='lightgreen')
plt.title('Bobot Dinamis Kriteria', pad=20)
plt.xlabel('Kriteria')
plt.ylabel('Bobot (0-1)')
plt.xticks(rotation=45, ha='right')
plt.grid(axis='y', linestyle='--', alpha=0.7)

# Tambah label nilai di atas bar (dalam persentase)
for bar in bars2:
    height = bar.get_height()
    plt.text(bar.get_x() + bar.get_width()/2., height,
             f'{height:.2f}\n({height*100:.1f}%)',
             ha='center', va='bottom')

plt.tight_layout()
plt.show()

# Tampilkan tabel hasil
print("\nTabel Hasil Pembobotan:")
print(weights_df.to_string(index=False))