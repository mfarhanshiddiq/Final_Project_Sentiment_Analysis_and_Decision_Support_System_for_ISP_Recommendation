def manual_stem(word):
    """Stemming manual dengan aturan afiks Bahasa Indonesia"""
    original_word = word

    # 1. Hapus konfiks (prioritas tertinggi)
    for prefix, suffix in sorted(AFFIXES['confix'], key=lambda x: -(len(x[0])+len(x[1]))):
        if word.startswith(prefix) and word.endswith(suffix) and len(word) > len(prefix)+len(suffix):
            word = word[len(prefix):-len(suffix)]
            break

    # 2. Hapus prefix
    for prefix in sorted(AFFIXES['prefix'], key=len, reverse=True):
        if word.startswith(prefix) and len(word) > len(prefix):
            word = word[len(prefix):]
            break

    # 3. Hapus suffix
    for suffix in sorted(AFFIXES['suffix'], key=len, reverse=True):
        if word.endswith(suffix) and len(word) > len(suffix):
            word = word[:-len(suffix)]
            break

    # Jika hasil stemming terlalu pendek (<3 huruf), kembalikan ke aslinya
    return word if len(word) >= 3 else original_word

def hybrid_stem(text):
    """Gabungkan Sastrawi dan manual stemming"""
    if not isinstance(text, str):
        return []

    tokens = word_tokenize(text.lower())
    processed_tokens = []

    for token in tokens:
        # Pertama coba Sastrawi
        sastrawi_result = sastrawi_stemmer.stem(token)

        # Jika Sastrawi tidak mengubah kata (atau perubahan kecil), gunakan manual
        if sastrawi_result == token or len(sastrawi_result)/len(token) > 0.8:
            manual_result = manual_stem(token)
            # Pilih yang paling efektif (menghasilkan kata lebih pendek)
            stemmed = min([sastrawi_result, manual_result], key=len)
        else:
            stemmed = sastrawi_result

        # Simpan hanya token yang valid
        if len(stemmed) >= 2:  # Minimal 2 karakter
            processed_tokens.append(stemmed)

    return processed_tokens

